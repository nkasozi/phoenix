import { FormValidationResult } from "@mantine/form/lib/types";
import { SetStateAction } from "react";

// Handle Gather Save
export const handleGatherSave = async (
	resource: string,
	isValid: any,
	projectid: string,
	setLoading: (value: SetStateAction<boolean>) => void,
	mutate: any,
	translate: any,
	formValues: any,
	setInputList: (value: SetStateAction<string[]>) => void,
	reset: () => void,
	router: any,
	validate: () => FormValidationResult,
	id?: string
) => {
	if (isValid()) {
		if (projectid) {
			setLoading(true);
			mutate(
				{
					resource,
					id,
					values: formValues,
					errorNotification: (res: any) => {
						let message = translate("gathers.errors.create");
						if (res?.response?.data?.detail[0]?.msg) {
							message = res.response.data.detail[0].msg;
						}
						return {
							message,
							description: "Error",
							type: "error",
						};
					},
					successNotification: () => {
						let message = "";
						if (id) {
							message = translate("gathers.success.update");
						} else {
							message = translate("gathers.success.create");
						}
						return {
							message,
							description: "Success",
							type: "success",
						};
					},
				},
				{
					onSuccess: async (res: any) => {
						await Promise.all([setInputList([]), reset()]);
						setTimeout(() => {
							router.push(
								`/projects/${projectid}/gathers/${res?.data?.child_type}/${res?.data?.id}`
							);
						}, 1000);
					},
					onError: () => {
						setLoading(false);
					},
				}
			);
		}
	} else {
		validate();
	}
};

